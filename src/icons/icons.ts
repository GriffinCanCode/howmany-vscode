/**
 * Helper function to create an SVG icon element
 */
export function createIcon(iconName: keyof typeof Icons, className?: string): string {
    const svg = Icons[iconName];
    if (className) {
        return svg.replace('<svg', `<svg class="${className}"`);
    }
    return svg;
}

/**
 * Helper function to create an inline SVG icon for VS Code webviews
 */
export function inlineIcon(iconName: keyof typeof Icons, size = 16): string {
    return Icons[iconName]
        .replace('width="16"', `width="${size}"`)
        .replace('height="16"', `height="${size}"`);
} 